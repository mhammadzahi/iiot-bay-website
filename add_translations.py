def update_po_file():
    """Update the .po file with Arabic translations"""
    po_file = "translations/ar/LC_MESSAGES/messages.po"
    
    with open(po_file, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Replace empty msgstr with translations
    for english, arabic in translations.items():
        # Escape quotes in the strings
        english_escaped = english.replace('"', '\\"').replace("'", "\\'")
        arabic_escaped = arabic.replace('"', '\\"')
        
        # Find the pattern: msgid "English text"\nmsgstr ""
        pattern = f'msgid "{english_escaped}"\\nmsgstr ""'
        replacement = f'msgid "{english_escaped}"\\nmsgstr "{arabic_escaped}"'
        
        if pattern in content:
            content = content.replace(pattern, replacement)
            print(f"✓ Translated: {english[:50]}...")
    
    with open(po_file, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f"\n✅ Updated {po_file} with {len(translations)} translations")

if __name__ == "__main__":
    update_po_file()
